import java.io.*;

public class T_22 {

    static long part2num(int n, int k, long num) {
        if (n == 0 && k == 0) {
            num += 1;
        }
        if (k > 0 && n >= k) {
            num += part2num(n, k - 1, num) + part2num(n - k, k, num);
        }
        if (k > n) {
            num += part2num(n, n, num);
        }
        return num;
    }

    public static void main(String[] args) throws IOException {
        try (BufferedReader br = new BufferedReader(new InputStreamReader(new FileInputStream("part2num.in")));
             PrintWriter pr = new PrintWriter("part2num.out")) {
            String[] buf = br.readLine().split("[^0-9]");
            int n = 0;
            int k = Integer.parseInt(buf[0]);;
            for (int i = 0; i < buf.length; i++) {
                n += Integer.parseInt(buf[i]);
                if (Integer.parseInt(buf[i]) >  k) {
                    k = Integer.parseInt(buf[i]);
                }
            }
            long num;
            //????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            //????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            //????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            if (k == 1) {
                num = 0;
            }
            else {
                num = part2num(n, k, 0);
            }
            pr.println(num);
        }
    }
}
